def main():
    csv_path = os.path.join(os.path.dirname(__file__), "..", "outputs", "full_ky_vocab.csv")
    with open(csv_path, "r", encoding="utf-8") as f:
        lines = f.readlines()

    num_data = len(lines) - 1
    if num_data <= 0 or len(EXTRA_PEIDAO) < num_data:
        raise SystemExit(f"Need {num_data} extra pairs, have {len(EXTRA_PEIDAO)}")

    out_lines = [lines[0]]  # header unchanged
    for i, line in enumerate(lines[1:], start=0):
        line = line.rstrip("\n")
        if not line:
            out_lines.append(line)
            continue
        parts = line.split(",", 8)
        if len(parts) < 8:
            out_lines.append(line)
            continue
        ex2, ex3 = EXTRA_PEIDAO[i]
        peidao = parts[6]
        new_peidao = f"{peidao}；{ex2}；{ex3}"
        parts[6] = new_peidao
        out_lines.append(",".join(parts) + "\n")

    with open(csv_path, "w", encoding="utf-8") as f:
        f.writelines(out_lines)
    print(f"Done: added two 搭配 examples to each of {num_data} rows (three total per row).")

if __name__ == "__main__":
    main()
